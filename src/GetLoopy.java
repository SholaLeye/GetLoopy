public class GetLoopy {
    public static void main(String[] args) {

        // Task 1: Count up by 1 from 0 to 30
        System.out.println("Task 1: Count up from 0 to 30");
        for (int i = 0; i <= 30; i++) {
            System.out.print(i + " ");
        }
        System.out.println("\n"); // new line

    }
}